first_list = [1, 2, 3]
second_list = [4, 5, 6]

# RAdding
first_list.append(100)
second_list.insert(2, 99)
first_list.extend([101, 102])
print(first_list)
print(second_list)

# Removing
third_list = [10, 11, 12]
fourth_list = ['apple', 'orange', 'banana']

third_list.pop()
fourth_list.sort()
print(third_list)
print(fourth_list)
